          <p><strong>Tentang Perusahaan</strong></p>
            <p style="text-align: justify; margin-left: 100px;">PT Bukit Asam, Tbk merupakan salah satu perusahaan milik BUMN (Badan Usaha Milik Negara) yang didirikan pada tahun 1950 dan bergerak dibidang energi pertambangan batu bara. Kantor pusat Bukit Asam berlokasi di Jln. Parigi No.1 Tanjung Enim 31716 Kecamatan Lawang Kidul, Kabupaten Muara Enim, Sumatera Selatan. PT Bukit Asam, Tbk mempunyai tugas dan fungsi yang diatur dalam Undang-undang guna melaksanakan rangkaian program pengembangan masyarakat yang didukung kemampuan internal dari laba perusahaan.</p>
            <p style="text-align: justify; margin-left: 100px;">Sebagai perusahaan tambang dengan area kelolaan yang luas, PT Bukit Asam, Tbk melaksanakan tanggung jawab sosial perusahaan atau CSR (Corporate Social Responsibility) dengan terus mendorong pertumbuhan ekonomi dan membangun kemandirian masyarakat serta berupaya memperbaiki kualitas lingkungan hidup. Salah satu program unggulan dari CSR (Corporate Social Responsibility) di bidang pendidikan yaitu menyelenggarakan beasiswa Biaya Pendidikan Sekitar Bukit Asam atau dikenal dengan beasiswa bidiksiba. Beasiswa bidiksiba merupakan program beasiswa pendidikan yang di berikan PT Bukit Asam, Tbk kepada siswa/i lulusan SLTA/Sederajat yang berprestasi secara akademik dan berlatar belakang keluarga kurang mampu disekitar wilayah Bukit Asam serta lulus pada seleksi masuk perguruan tinggi yang dipersiapkan untuk menempuh pendidikan selama tiga tahun dalam Program Studi Diploma III.</p>
            <p style="text-align: justify; margin-left: 100px;"> Pada program beasiswa ini, PTBA menjalin kerjasama dengan dua perguruan tinggi, yaitu Politeknik Negeri Sriwijaya Palembang (Polsri) dan Politeknik Negeri Malang (Polinema). Beasiswa ini diadakan setiap 1 tahun sekali dengan jumlah pendaftar mencapai ratusan siswa per tahunnya dan pendaftar pada Beasiswa ini hanya siswa yang berdomisili di wilayah Ring atau wilayah operasional perusahaa. Untuk menerima Beasiswa Bidiksiba, siswa diharuskan untuk mengikuti prosedur yang telah ditentukan. Dimulai dari pendaftaran seleksi administrasi hingga survey yang dilaksanakan oleh tim pelaksana bidiksiba. </p>
